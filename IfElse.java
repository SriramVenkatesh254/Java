class IfElse{
    public static void main(String a[]){
        int x = 1;
        int y = 2;

        if( x > y){
            //This block will get executed if the condition is true.
            System.out.println(x);
            System.out.println("Thank You");
        }
        else{
            //This block will get executed if the condition if false.
            System.out.println(y);
            System.out.println("Thank You");
        }
    }
}